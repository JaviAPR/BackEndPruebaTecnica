package com.backend.backendPruMacondo.config;

import org.springframework.context.annotation.Configuration;
import org.springframework.web.servlet.config.annotation.CorsRegistry;
import org.springframework.web.servlet.config.annotation.WebMvcConfigurer;

@Configuration
public class WebConfig implements WebMvcConfigurer {

    @Override
    public void addCorsMappings(CorsRegistry registry) {
        registry.addMapping("/**") // Permite todos los caminos
                .allowedOrigins("http://localhost:9000") // Permite el origen del frontend
                .allowedMethods("GET", "POST", "PUT", "DELETE", "OPTIONS") // MÃ©todos permitidos
                .allowedHeaders("*") // Permite todos los encabezados
                .allowCredentials(true); // Permite credenciales si es necesario
    }
}
